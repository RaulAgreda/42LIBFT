int	ft_atoi(const char *str)
{
	int	neg;
	int	n;

	neg = *str == '-';
	if (neg)
		str++;
	n = 0;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + (*str - '0');
	}
	if (neg)
		return (-n);
	return (n);
}
